#include <stdlib.h>

char	*ft_strnew(size_t size)
{
	char			*str;
	unsigned long	i;

	str = malloc(sizeof(char) * size);
	if (!str)
		return (NULL);
	i = 0;
	while (i < size)
	{
		str[i] = '\0';
		i++;
	}
	return (str);
}
